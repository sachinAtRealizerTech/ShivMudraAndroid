public class DiscountModel {

    public String name;
    public String date;
    public String percenatage;
    public String value;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getPercenatage() {
        return percenatage;
    }

    public void setPercenatage(String percenatage) {
        this.percenatage = percenatage;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
